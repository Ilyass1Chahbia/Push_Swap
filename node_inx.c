#include "push_swap.h"

void	ft_node_indx(t_stack **a)
{
	t_stack	*tmp;
	int		j;

	j = 0;
	tmp = *a;
	while (tmp)
	{
		tmp->i = j;
		j++;
		tmp = tmp->next;
	}
}
